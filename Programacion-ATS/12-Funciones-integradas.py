# Funciones integradas

n = int("10")  # Valor numero
n2 = str(20)  # Convierte el numero en un string
n3 = bin(10)  # Convierte el numero en decimal
n4 = hex(10)  # Convierte en numero Hexadecimal
n5 = int("0b1010", 2)  # Un Binario en un entero
n6 = abs(-8)  # Valor absoluto de un numero
n7 = round(5.6)  # Redondea hacia el mas cercano

print(n)
